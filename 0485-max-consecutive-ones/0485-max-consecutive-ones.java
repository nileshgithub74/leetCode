class Solution {
    public int findMaxConsecutiveOnes(int[] arr) {
        int n = arr.length;

        int maxCount =0;
        int count =0;

        for(int i =0; i < n;i++){

            if(arr[i] == 1){
                count++;
                maxCount = Math.max(maxCount, count);
            }else{
                 count =0;
            }
        } 
        return maxCount;
        
    }
}